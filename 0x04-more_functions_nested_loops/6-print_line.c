#include "main.h"

/**
 *print_line - Entry point
 *Return: Always 0 (success)
 *@n: return value
 *Description: write a C program that prints lines
*/

void print_line(int n)
{
	for (n = 0; n <= 0; n++)
	{
		_putchar('\n');
	}

	n = 1;

	while (n <= 10)
	{
		_putchar('_');
		n++;
	}
		_putchar('\n');
}
