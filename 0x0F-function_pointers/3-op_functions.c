#include "calc.h"
/**
 *op_add - adds two ints
 *@a: first int
 *@b:second int
 *Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *op_sub - subtracts
 *@a: first
 *@b: second
 *Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 *op_mul - multiplies
 *@a: first
 *@b: second
 *Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 *op_div - divides
 *@a: first
 *@b: second
 *Return: quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 *op_mod - checks for remainder
 *@a: first
 *@b: second
 *Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}

